    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        int selectRow = tblVaccineStorage.getSelectedRow();
        //VaccineManufacturer e =(VaccineManufacturer)enterprise;
        if(selectRow>=0){
            String name= (String) tblVaccineStorage.getValueAt(selectRow, 0);
            int BatchID= (int) tblVaccineStorage.getValueAt(selectRow, 1);
            int Quantity = (int) tblVaccineStorage.getValueAt(selectRow, 2);
            String status= (String) tblVaccineStorage.getValueAt(selectRow, 3);
            
            txtNameVP.setText(name);
            txtBatchNoVp.setText(String.valueOf(BatchID));
            txtQuantityVP.setText(String.valueOf(Quantity));
            txtStatusVP.setText(status);
        }
        else {
            JOptionPane.showMessageDialog(null,"Please select a row");
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        VaccineProvider e =(VaccineProvider)enterprise;
        int qty=Integer.parseInt(txtQuantityVP.getText());
        int batchId=Integer.parseInt(txtBatchNoVp.getText());
        String status=txtStatusVP.getText();
        String name=txtNameVP.getText();
        
        for(Vaccine v: e.getVaccineDirectory().getVaccDir()){
            if(v.getBatchNo()==batchId){
                v.setVacName(name);
                v.setQuantity(qty);
                v.setVacStatus(status);
            }
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        VaccineProvider e =(VaccineProvider)enterprise;
        int batchId=Integer.parseInt(txtBatchNoVp.getText());
        for(Vaccine v: e.getVaccineDirectory().getVaccDir()){
            if(v.getBatchNo()==batchId){
                e.getVaccineDirectory().getVaccDir().remove(v);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

