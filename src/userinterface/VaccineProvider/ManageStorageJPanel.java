    private void populateTable() {
         DefaultTableModel dtm = (DefaultTableModel) tblVaccineStorage.getModel();
        dtm.setRowCount(0);

                VaccineProvider vm =(VaccineProvider)enterprise;
                for (Vaccine vaccine : vm.getVaccineDirectory().getVaccDir()) {
                    
                    Object[] row = new Object[4];
                    row[0] = vaccine.getVacName();
                    row[1] = vaccine.getBatchNo();
                    row[2] = vaccine.getQuantity();
                    row[3] = vaccine.getVacStatus();
                   
                   

                    dtm.addRow(row);
                
                } //To change body of generated methods, choose Tools | Templates.
    }
